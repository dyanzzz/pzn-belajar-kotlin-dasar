fun main() {
    // 11. operasi perbandingan
    
    // >, <, >=, <=, ==, !=

    val nilaiA = 100
    val nilaiB = 200

    val perbandingan: Boolean = nilaiA < nilaiB
    println(perbandingan)

    // perbandingan bisa juga digunakan untuk string
    val a = "abc"
    val b = "def"
    println(a != b)
}